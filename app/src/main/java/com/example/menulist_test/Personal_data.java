package com.example.menulist_test;

public class Personal_data {
    String cup;
    String account_id;
    String menu_id;
    String shot;
    String syrup;
    String whipped_cream;
    String drizzle;
    String size;
    String option_sum;
    String price;
    String menu_name;
    String image;

    public Personal_data(String cup, String account_id, String menu_id, String shot, String syrup, String whipped_cream, String drizzle, String size, String option_sum, String price, String menu_name, String image) {
        this.cup = cup;
        this.account_id = account_id;
        this.menu_id = menu_id;
        this.shot = shot;
        this.syrup = syrup;
        this.whipped_cream = whipped_cream;
        this.drizzle = drizzle;
        this.size = size;
        this.option_sum = option_sum;
        this.price = price;
        this.menu_name = menu_name;
        this.image = image;
    }

    public String getCup() {
        return cup;
    }

    public void setCup(String cup) {
        this.cup = cup;
    }

    public String getAccount_id() {
        return account_id;
    }

    public void setAccount_id(String account_id) {
        this.account_id = account_id;
    }

    public String getMenu_id() {
        return menu_id;
    }

    public void setMenu_id(String menu_id) {
        this.menu_id = menu_id;
    }

    public String getShot() {
        return shot;
    }

    public void setShot(String shot) {
        this.shot = shot;
    }

    public String getSyrup() {
        return syrup;
    }

    public void setSyrup(String syrup) {
        this.syrup = syrup;
    }

    public String getWhipped_cream() {
        return whipped_cream;
    }

    public void setWhipped_cream(String whipped_cream) {
        this.whipped_cream = whipped_cream;
    }

    public String getDrizzle() {
        return drizzle;
    }

    public void setDrizzle(String drizzle) {
        this.drizzle = drizzle;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getOption_sum() {
        return option_sum;
    }

    public void setOption_sum(String option_sum) {
        this.option_sum = option_sum;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getMenu_name() {
        return menu_name;
    }

    public void setMenu_name(String menu_name) {
        this.menu_name = menu_name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
